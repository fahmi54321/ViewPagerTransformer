package com.android.a12viewpagertransform.Model;

public class PlatformData {

    public int image;
    public String name;

    public PlatformData() {
    }

    public PlatformData(int image, String name) {
        this.image = image;
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
